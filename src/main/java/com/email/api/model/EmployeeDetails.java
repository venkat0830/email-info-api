package com.email.api.model;

public class EmployeeDetails {
	
	
	private String employeeId;
	private String employeeFName;
	private String employeeLName;
	private String employeeSal;
	private String employeeDesg;
	private String employeeDept;
	public String getEmployeeId() {
		return employeeId;
	}
	public void setEmployeeId(String employeeId) {
		this.employeeId = employeeId;
	}
	public String getEmployeeFName() {
		return employeeFName;
	}
	public void setEmployeeFName(String employeeFName) {
		this.employeeFName = employeeFName;
	}
	public String getEmployeeLName() {
		return employeeLName;
	}
	public void setEmployeeLName(String employeeLName) {
		this.employeeLName = employeeLName;
	}

	public String getEmployeeDesg() {
		return employeeDesg;
	}
	public void setEmployeeDesg(String employeeDesg) {
		this.employeeDesg = employeeDesg;
	}
	public String getEmployeeDept() {
		return employeeDept;
	}
	public void setEmployeeDept(String employeeDept) {
		this.employeeDept = employeeDept;
	}
	public String getEmployeeSal() {
		return employeeSal;
	}
	public void setEmployeeSal(String employeeSal) {
		this.employeeSal = employeeSal;
	}
	
	

}
